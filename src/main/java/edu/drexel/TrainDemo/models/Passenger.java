package edu.drexel.TrainDemo.models;

public class Passenger{
    private String firstName;
    private String lastName;
    private Date dob;
    private String address;
    private String city;
    private State state;
    private String zip;
    private String phoneNo;
    private String emailAddress;

    public getFirstName(){
        return firstName;
    }

    public getLastName(){
        return lastName;
    }

    public getDob(){
        return dob;
    }

    public getAddress(){
        return address;
    }

    public getCity(){
        return city;
    }

    public getState(){
        return state;
    }

    public getZip(){
        return zip;
    }

    public getPhoneNo(){
        return phoneNo;
    }

    public getEmailAddress(){
        return emailAddress;
    }

}