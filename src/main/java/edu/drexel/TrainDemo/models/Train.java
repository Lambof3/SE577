package edu.drexel.TrainDemo.models;

public class Train {

    private String name;
    private int capacity;
    private int coachSeats;
    private int economySeats;

    public getName(){
        return name;
    }

    public getCapacity(){
        return capacity;
    }

    public getCoachSeats(){
        return coachSeats;
    }

    public getEconomySeats(){
        return economySeats;
    }

}