package edu.drexel.TrainDemo.models;

public class TrainStation {

    private String name;
    private String abbrName;
    private String address;
    private String city;
    private State state;
    private String zip;


    public getName(){
        return name;
    }

    public getAbbrName(){
        return abbrName;
    }

    public getAddress(){
        return address;
    }

    public getCity(){
        return city;
    }

    public getState(){
        return state;
    }

    public getZip(){
        return zip;
    }

}